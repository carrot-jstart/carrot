export const localeMessages: Record<Locale, Messages> = {
    "zh-CN": zhCN,
    "en-US": enUS
};

export function resolveLocale(input?: string | null): Locale {
    const value = (input || "").toLowerCase();
    return value.startsWith("zh") ? "zh-CN" : "en-US";
}

export function readPreferredLocale(): Locale {
    if (typeof window === "undefined") {
        return "zh-CN";
    }
    const stored = window.localStorage.getItem(I18N_STORAGE_KEY);
    if (stored === "zh-CN" || stored === "en-US") {
        return stored;
    }
    return resolveLocale(window.navigator.language);
}

export function getMessage(locale: Locale, key: string, vars?: Record<string, string | number>) {
    const template = localeMessages[locale][key] ?? localeMessages["zh-CN"][key] ?? key;
    if (!vars) {
        return template;
    }
    return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ""));
}
